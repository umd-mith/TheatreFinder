            <h3>Franklin J. Hildy, General Editor</h3>
            <p>Franklin J. Hildy is Professor of Theatre and Director of the PhD Program in the School 
of Theatre, Dance and Performance Studies at the University of Maryland, College Park. He also serves 
as the Director of Shakespeare’s Globe (USA) Research Archive. He works as a theatre consultant 
for historic theatre preservation and reconstruction, and is a member of the Architecture Research 
Group for Shakespeare’s Globe in London. He is the founding convenor of the architecture working group for the International Federation for Theatre Research (IFTR).</p>
<p>Dr. Hildy has conducted field research on historic theatres in twenty one countries including  Austria 1989, 2002; Croatia 1986; Czech Republic 1994, 2002, 2007; 
Denmark 1973, 2003, 2009; England 1973, 1978-79, 1983, 1986, 1988 and twice a year since then; France 1973, 1983, 1989, 2004; 
Germany 1983, 1989, 2002, 2003; Greece 1979, 1993, 1999; Israel 2006; Italy 1974, 1983, 1994, 2006; Japan 1991; 
Jordan 2006; Korea 2008, Malta 1994; Portugal 1987, 2009; Russia 2004; Spain 1974, 1987, 1989, 2001, 
2009, 2010; South Africa 2007; Sweden 1973, 1983, 2006; Taiwan 2002, 2003, 2008; and Turkey 1999.</p>
<p>Dr. Hildy has published over 50 articles, 24 of which have been on theatre architecture. He has also delivered 
more than 40 papers at national and international conferences on specific theatre structures and the way 
in which those buildings define audiences. As co-author with Oscar G. Brockett of <em>The History of Theatre</em>, 
a work generally known as the “bible of theatre history,” his writing on theatre architecture is read 
worldwide. He serves as convener of the Theatre Architecture Working Group for the International 
Federation for Theatre Research, is a member of the International Organization of Scenographers, 
Theatre Architects and Technicians (OISTAT) and an associate of  Perspectiv—The Association of 
Historic Theatres in Europe.</p>
