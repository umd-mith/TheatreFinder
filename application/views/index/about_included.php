            <h3>What's Included in Theatre Finder?</h3>
                                <p>To be included in theatre-finder.org, the theatre must be 100 years old or older and the auditorium must be in a sufficient state of preservation to allow the average visitor to clearly make out its architectural form.  We do not list buildings that once housed a theatre but no longer contain an intact auditorium and we do not include archaeological remains in which the shape of the auditorium is not clearly visible.</p>
                                <p>Buildings that were built for another purpose but were occasionally used for the performance of live theatre are only included if they are significant to later developments in theatre architecture. Cinema?~@~Ys (motion picture houses) anatomy theatres, music and concert halls, circuses, and other such building that were not specifically designed for the performance of live theatre are generally not given an entry, but often turn up under ?~@~\related cites nearby.?~@~] A major exception has been the inclusion of ancient odeons (designed for music) and <em>bouleuterions</em>, (designed for councilmeeeting) which have been included because they are often mistaken for theatres and it can be confusing not to identify them on an archaeological site. Their entries, however, are less thorough than those given to regular theatres.</p>
                                <p>To facilitate searches, we have used Odeion to identify Greek structures of this type and Odeum to identify the Roman ones. When speaking generically of this type of building, we have used the English term Odeon.
                                <p>Historically accurate reconstructions are included for their experiential value and are clearly identified as reconstructions.</p>
