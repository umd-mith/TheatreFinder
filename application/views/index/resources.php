        <h1>Resources</h1> 
        <h3></h3> 
		<p>To enhance the value of theatre-finder.org we intend to offer information of special interest to researchers, especially those interested in proposing a restoration or preservation project for an historic theatre.</p> 
			<p>While we have not set the parameters of all this might include, we expect to have <strong>a master bibliography</strong> which will include general works on the subject of historic theatre 
				architecture, all works listed in the bibliographies provided for each theatre that has been 
				given a full entry, and works that relate to theatres that have not received a full entry in 
				theatre-finder.org.</p> 
				<p>We also hope to include <strong>primary documents</strong>, 
				<strong>conference papers</strong> submitted to us by interested scholars, and 
				<strong>research/conservation reports</strong> developed by those proposing theatre renovations. Each entry will be in standard bibliographical 
				form but will end in a two letter code that will identify the country of the theatre it covers or will designate it as a 
				general work or multi theatre work.</p> 
				<p>We will also identify each entry by type so the bibliography 
				can be easily sorted. If there is a digital text available on our web site or elsewhere on the 
				world wide web, we will link to that text.
