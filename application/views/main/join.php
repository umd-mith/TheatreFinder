            <h3>Contributor guidelines</h3>
            <p>This project is designed to be an outlet for individuals world wide who have expert knowledge on a theatre or group of theatres, or would like to develop such expertise by researching the details of theatres which have auditoriums that are more than 100 years old. Each entry will include the name of the author who wrote it.</p>
            <p>If there is a  theatre in theatre-finder.org that you would like to write an entry for, please apply to the General Editor at the email address below. Please include the following information in your email:</p>
				<ul>
					<li>your complete contact information</li>
					<li>the name of the theatre</li>
					<li>the country and city of the theatre</li>
					<li>a 250 word statement of your qualifications for writing the entry</li>
					<li>a short  bibliography of sources you would use</li>
				</ul>
			<p>Your application will be reviewed by the editorial board. Should your application be approved, you will be sent instructions on how to develop your entry and a code that will allow you access to the editing page.</p>
            <p>If the theatre you wish to write about is not already listed in theatre-finder.org and you have evidence that it should be on the list because its auditorium is more that 100 years old, please provide this evidence with your application.</p>
            <p>If the theatre you wish to write about already has a full entry and you want to offer corrections, please send the suggested corrections to the General Editor at the email address below.</p>
